package africanroyals.model;

import jakarta.persistence.*;

@Entity
@Table(name = "users") // make sure this table exists in SQLite or let JPA create it
public class User {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false, unique = true)
    private String email;

    @Column(nullable = false, unique = true)
    private String username;

    @Column(nullable = false)
    private String fullName;

    @Column(nullable = false)
    private String password; // you can hash later

    @Column(nullable = false)
    private String role; // "USER" or "ADMIN"

    public User() {
    }

    public User(String email, String username, String fullName, String password, String role) {
        this.email = email;
        this.username = username;
        this.fullName = fullName;
        this.password = password;
        this.role = role;
    }

    // getters & setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
